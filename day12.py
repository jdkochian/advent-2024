def getLegalNeighbors(i, j, m, n): 
    res = [] 
    for delY, delX in [(0, -1), (-1, 0), (1, 0), (0, 1)]: 
        new_y, new_x = i + delY, j + delX
        if new_y >= 0 and new_x >= 0 and new_y < m and new_x < n: 
            res.append((new_y, new_x))

    return res

def findAreaPerimiterOfRegionRec(i, j, m, n, board, visited):
    a = 0 
    p = 0 

    curr = board[i][j]

    def visit(i, j):
        nonlocal visited 
        nonlocal a
        nonlocal p
        if (i, j) in visited: 
            return

        visited.add((i, j))

        a += 1
        for delY, delX in [(0, -1), (-1, 0), (1, 0), (0, 1)]: 
            new_y, new_x = i + delY, j + delX 
            if new_y >= 0 and new_x >= 0 and new_y < m and new_x < n: 
                if board[new_y][new_x] == curr: 
                    visit(new_y, new_x)
                else: 
                    p += 1
            else: 
                p += 1
    
    visit(i, j)

    return a, p


def puzzle_1(board : list[list[str]]): 
    _sum = 0 
    visited = set() 

    m = len(board)
    n = len(board[0])

    for i in range(m): 
        for j in range(n): 
            if (i, j) not in visited: 
                a, p = findAreaPerimiterOfRegionRec(i, j, m, n, board, visited)
                _sum += a * p

    return _sum 

